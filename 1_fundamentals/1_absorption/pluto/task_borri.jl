# ╔═╡ ccfc8916-4939-4c5e-9f6d-cf38031b7b26
if enable_lecture
md"""
With our variables, this is eq. 1.30
```math
A_{21} = \frac{4}{3} \, \frac{n_r^3}{4 \pi \, \epsilon \epsilon_0} 
\, \frac{|\mu_{21}|^2 \omega^3}{\hbar c_0^3}
```
where we applied the 'in medium' rules: a) all $\epsilon_0$ get an $\epsilon$ and b) all $c$ are interpreted as $c_0 /n$
"""
end

# ╔═╡ ff921a74-744d-4275-9dbe-2615b8a1de36
md"""
Lets put in some numbers
"""

# ╔═╡ b06544b1-2a06-4bc8-bd79-8fffba4ddd2a
radiative_rate = 1/ 1u"ns";

# ╔═╡ ba2931c1-d042-4304-b2a8-3b7631f39116
n_ingaas = 3.5;

# ╔═╡ d0a16e08-1370-4f37-8d4f-ad280c2e7c8a
md"""
We need the energy / frequency / wavelength of the transition. You need to dig a bit in the paper. 

*The cell below sets $E$ to this value. Unhide it to check your solution.*
"""

# ╔═╡ ae87e7be-7ff2-4671-918d-d1127a2f6f74
begin
	E = 1.15u"eV"; # inset Fig.2b;
	ω = E / ħ |> u"Hz";
end;

# ╔═╡ a9f52458-0510-4d1d-949d-966b3681741e
md"""
Now we need to caclulate the transition dipole moment from these values. Convert it to Debye.

*The cell below contains the formula. Unhide it to check your solution.*
"""

# ╔═╡ f19f05d8-e4e4-4010-99d1-ad8826d6ed76
μ_21 = sqrt(radiative_rate * ħ * c_0^3 * 3 * π  * ε_0 / ( n_ingaas * ω^3)) |> u"D"

